const isFunction = arg => typeof arg === 'function'
const isObject = arg => typeof arg === 'object'
const isString = arg => typeof arg === 'string'
const isNumber = arg => typeof arg === 'number'
const isUndefined = arg => typeof arg === 'undefined'

export {
  isFunction,
  isObject,
  isString,
  isNumber,
  isUndefined
}
